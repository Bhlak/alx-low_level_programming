#include "3-calc.h"

/**
 * op_add - Addition
 * @a: First integer
 * @b: Second integer
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtraction
 * @a: First integer
 * @b: Second integer
 * Return: Difference
 */

int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - Multiplication
 * @a: First integer
 * @b: Second integer
 * Return: Product
 */

int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - Division
 * @a: First integer
 * @b: Second integer
 * Return: Quotient
 */

int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - Modulus
 * @a: First integer
 * @b: Second integer
 * Return: Remainder
 */

int op_mod(int a, int b)
{
        return (a % b);
}
